# ODE System (Mekanistik Model)
# ---------------------------------------------------------
def trueODEfunc_multiguild(t, y, p):
    y = np.maximum(0, y)

    H2      = y[0]
    CO2     = y[1]
    CH4     = y[2]
    H2S     = y[3]
    SO4     = y[4]
    FeS     = y[5]
    X_meth  = y[6]
    X_sulf  = y[7]
    X_aceto = y[8]
    Acetate = y[9]

    k_meth   = p[0]
    k_sulf   = p[1]
    k_aceto  = p[2]
    Y_m      = p[3]
    Y_s      = p[4]
    Y_a      = p[5]
    KI_meth  = p[6]
    KI_sulf  = p[7]
    KI_aceto = p[8]
    k_precip = p[9]
    H2S_sat  = p[10]
    H2_thresh= p[11]
    DG_thresh= p[12]

    R = 8.314e-3
    T = 298.15
    RT = R * T
    DG0_meth = -130
    DG0_sulf = -152
    DG0_aceto = -95

    eps = 1e-6
    H2_s      = max(H2, eps)
    CO2_s     = max(CO2, eps)
    CH4_s     = max(CH4, eps)
    SO4_s     = max(SO4, eps)
    H2S_s     = max(H2S, eps)
    Acetate_s = max(Acetate, eps)

    f_inh_meth  = KI_meth  / (KI_meth  + H2S)
    f_inh_sulf  = KI_sulf  / (KI_sulf  + H2S)
    f_inh_aceto = KI_aceto / (KI_aceto + H2S)

    f_activation = H2 / (H2 + H2_thresh)

    Q_meth  = CH4_s     / (H2_s**4 * CO2_s)
    Q_sulf  = H2S_s     / (H2_s**4 * SO4_s)
    Q_aceto = Acetate_s / (H2_s**4 * CO2_s**2)

    DG_meth  = DG0_meth  + RT * np.log(Q_meth)
    DG_sulf  = DG0_sulf  + RT * np.log(Q_sulf)
    DG_aceto = DG0_aceto + RT * np.log(Q_aceto)

    f_thermo_meth  = 1 / (1 + np.exp((DG_meth  - DG_thresh) / RT))
    f_thermo_sulf  = 1 / (1 + np.exp((DG_sulf  - DG_thresh) / RT))
    f_thermo_aceto = 1 / (1 + np.exp((DG_aceto - DG_thresh) / RT))

    r_meth  = k_meth  * H2 * (CO2_s**-2) * f_inh_meth  * f_activation * f_thermo_meth
    r_sulf  = k_sulf  * H2 * SO4      * f_inh_sulf  * f_activation * f_thermo_sulf
    r_aceto = k_aceto * H2 * (CO2**2) * f_inh_aceto * f_activation * f_thermo_aceto

    r_precip = k_precip * max(0, H2S - H2S_sat)

    dH2      = -4*r_meth - 4*r_sulf - 4*r_aceto
    dCO2     = -1*r_meth - 2*r_aceto
    dCH4     = +1*r_meth
    dH2S     = +1*r_sulf - r_precip
    dSO4     = -1*r_sulf
    dFeS     = +1*r_precip
    dX_meth  = Y_m * r_meth
    dX_sulf  = Y_s * r_sulf
    dX_aceto = Y_a * r_aceto
    dAcetate = +1*r_aceto

    return [dH2, dCO2, dCH4, dH2S, dSO4, dFeS, dX_meth, dX_sulf, dX_aceto, dAcetate]

