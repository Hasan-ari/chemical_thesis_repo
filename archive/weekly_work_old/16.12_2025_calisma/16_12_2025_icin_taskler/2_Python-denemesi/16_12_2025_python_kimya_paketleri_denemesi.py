# Cost Function (Residuals)
# ---------------------------------------------------------
def residuals_multiguild(p, t_exp, data_exp, x0):
    try:
        sol = solve_ivp(
            lambda t, y: trueODEfunc_multiguild(t, y, p),
            [0, t_exp[-1]],
            x0,
            method='Radau',
            t_eval=t_exp,
            rtol=1e-3,
            atol=1e-6
        )

        if not sol.success or len(sol.t) < len(t_exp):
            return np.ones(data_exp.size) * 10.0

        y_sim = sol.y.T[:, 0:5]

        log_sim = np.log1p(y_sim)
        log_exp = np.log1p(data_exp)

        weights = np.array([1, 1, 0.5, 0.5, 1])

        res = (log_sim - log_exp) * weights
        res_flat = res.flatten()

        if np.isnan(res_flat).any():
            return np.ones(data_exp.size) * 10.0

        return res_flat

    except Exception:
        return np.ones(data_exp.size) * 10.0

